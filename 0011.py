#Emtrar com um numero e imprimir o quadrado e a raiz quadrada
import math

numero=float(input("Insira um numero: \n"))
quadrado=numero*numero
raiz=math.sqrt(numero)

print("O numero inserido foi: \n", numero)
print("O quadrado do numero inserido é:  \n", quadrado)
print("A raiz quadrada do número inserido é: \n", raiz)

