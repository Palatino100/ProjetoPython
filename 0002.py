#a média aritmética de quatro notas fornecidas pelo usuário. O programa deve
#calcular a média e exibir o resultado:

print("Vamos Calcular as médias!\n")
nota01=float(input("Digite a primeira nota:"))
nota02=float(input("Digite a segunda nota:"))
nota03=float(input("Digite a terceira nota:"))
nota04=float(input("Digite a quarta nota:"))

media=(nota01+nota02+nota03+nota04)/4

print("Sua média é: ", media )










